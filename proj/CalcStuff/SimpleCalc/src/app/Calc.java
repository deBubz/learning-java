package app;

/**
 * calculation options
 */
public class Calc {
    public int add(int a, int b){
        return a + b;
    }

    public int minus(int a, int b){
        return a - b;
    }

    public int mult(int a, int b){
        return a*b;
    }

    public int divis(int a, int b){
        return a/b;
    }
    
    public int mod(int a, int b){
        return a%b;
    }
    
}